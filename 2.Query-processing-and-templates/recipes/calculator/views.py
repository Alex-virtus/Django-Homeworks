from django.shortcuts import render

DATA = {
    'omlet': {
        'яйца, шт': 2,
        'молоко, л': 0.1,
        'соль, ч.л.': 0.5,
    },
    'pasta': {
        'макароны, г': 0.3,
        'сыр, г': 0.05,
    },
    'buter': {
        'хлеб, ломтик': 1,
        'колбаса, ломтик': 1,
        'сыр, ломтик': 1,
        'помидор, ломтик': 1,
    },
    # можете добавить свои рецепты ;)
}


# Напишите ваш обработчик. Используйте DATA как источник данных
# Результат - render(request, 'calculator/index.html', context)
# В качестве контекста должен быть передан словарь с рецептом:
# context = {
#   'recipe': {
#     'ингредиент1': количество1,
#     'ингредиент2': количество2,
#   }
# }

def recipe(request, name: str):
    """
    Обрабатывает HTTP-запрос и возвращает страницу с рецептом.

        Функция:
        - получает название рецепта из URL (name)
        - читает параметр servings (количество порций) из GET-запроса
        - находит рецепт в словаре DATA
        - пересчитывает количество ингредиентов в зависимости от порций
        - передает результат в HTML-шаблон

        Параметры:
        request — объект HTTP-запроса
        name (str) — название рецепта

        Возвращает:
        HttpResponse с отрендеренным шаблоном
    """

    # Получаем количество порций из GET-параметра (?servings=...)
    # Если параметр отсутствует — используем значение по умолчанию 1
    servings = int(request.GET.get("servings", 1))

    # Получаем рецепт по названию из словаря DATA
    # Если рецепта нет — возвращается пустой словарь
    recipe_data = DATA.get(name, {})

    # Создаем словарь для пересчитанных ингредиентов
    result = {}

    # Проходим по всем ингредиентам рецепта
    for ingredient, amount in recipe_data.items():
        # Умножаем количество каждого ингредиента на число порций
        result[ingredient] = amount * servings

    # Формируем контекст для передачи в шаблон
    context = {"recipe": result}

    # Возвращаем html-страницу с переданными данными
    return render(request, 'calculator/index.html', context)
